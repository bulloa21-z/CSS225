# The program that will compute MPG for a car

# Enter the number of miles driven and gallons used
miles_driven = float("Enter the number of miles driven: ")
gallons_used = float(input("Enter the number of gallons used: "))

# Calculate the MPG of the car
mpg = miles_driven / gallons_used

# Print the answer
print(f"Your car's MPG is {mpg: 2f} miles per gallon")
